  package univas;
   
import java.util.Scanner;

public class Calculadora {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		
		System.out.print("Escolha o primeiro numero: ");
		int n1 = scanner.nextInt();
		System.out.print("Escolha o segundo numero: ");
		int n2 = scanner.nextInt(); 
		
		
		System.out.println("Escolha a operação matemática: ");
		System.out.println("1 para soma, 2 para subtração");
		System.out.println("3 para multiplicação 4 pra divisão");
		int operação = scanner.nextInt();
		double total = 0;
		if(operação == 1) {
			 total = n1 + n2;
		}
		else if(operação == 2) {
			 total = n1 - n2;
		}
		else if(operação == 3) {
			 total = n1 * n2;
		}
		else if(operação == 4) {
			total = n1 / n2;
		}
		else {
			System.out.print("Vai da não");
		}
		System.out.print("O resultado é: " + total);
	   }
	
	}
